class Solution{
  public:
  
    //Function to find starting point where the truck can start to get through
    //the complete circle without exhausting its petrol in between.
    int tour(petrolPump p[],int n)
    {
       //Your code here
       int start=0;
       
       int balance=0;
       int needed=0;
       for(int i=0;i<n;i++){
           int pet=p[i].petrol;
           int dis=p[i].distance;
           balance+=pet-dis;
           if(balance<0){
               needed+=balance;
               balance=0;
               start=i+1;
           }
           
       }
       if(balance+needed>=0) return start;
       return -1;
    }
};



