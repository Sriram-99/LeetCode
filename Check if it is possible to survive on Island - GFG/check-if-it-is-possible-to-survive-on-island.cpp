class Solution{
public:
    int minimumDays(int S, int N, int M){
        if(M>N) return -1;
        int fd=S*M;
        int ans=fd/N;
        if(fd%N >0) ans++;
        int buying= S-(S/7);
        if(ans<=buying) return ans;
        
        return -1;
    }
};

